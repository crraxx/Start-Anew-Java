package com.ff.java.Day3;

import java.sql.SQLOutput;

public class OperDemo5 {
    /*位运算符
    >> , << , >>> ,& , | , ~ , ^
    */


    public static void main(String[] args) {
        int a=2;
        int b=7;
        System.out.println(a<<2);
        System.out.println(8>>>2);
        System.out.println(8>>2);
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
    }
}
